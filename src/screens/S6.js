import React, { useState } from "react";
import {
  View,
  Text,
  TouchableOpacity,
  StyleSheet,
  Dimensions,
} from "react-native";
import { useNavigation } from "@react-navigation/native";
import Background from "../Background";
import { ScrollView } from "react-native-gesture-handler";
import colors from "../Constants";
import Icon, { Icons } from "../bottomtab/Icon";

const windowWidth = Dimensions.get("window").width;
const windowHeight = Dimensions.get("window").height;

const S6 = (props) => {
  const [currentDayIndex, setCurrentDayIndex] = useState(0);
  const [selectedEventIndex, setSelectedEventIndex] = useState(-1);
  const navigation = useNavigation();

  const handlePreviousDay = () => {
    setCurrentDayIndex((prevIndex) => (prevIndex === 0 ? 3 : prevIndex - 1));
    setSelectedEventIndex(-1);
  };

  const handleNextDay = () => {
    setCurrentDayIndex((prevIndex) => (prevIndex === 3 ? 0 : prevIndex + 1));
    setSelectedEventIndex(-1);
  };

  const handleEventClick = (eventIndex) => {
    setSelectedEventIndex(eventIndex);
  };

  const eventTimelines = [
    [
      {
        eventName:
          "S6.1. Martina Andellini, Massimiliano Angelini, Matteo Ritrovato, Riccardo Schiaffini, Ernesto Iadanza, Leandro Pecchia, Cost-effectiveness of Continuous Glucose Monitoring For Paediatric Patients With Type 1 Diabetes Compared With Self-Monitoring Of Blood Glucose",
      },
      {
        eventName:
          "S6.2. Sejal S Shah, Nilesh Rathi, Nankar Meenakshi Y, Vini Mehta, Prevalence of electronic gadgets usage and its impact on sleep patterns amongst 7–10-year-old children during covid- 19 pandemic: A cross-sectional observational study",
      },
      {
        eventName:
          "S6.3. Wanzi Su, Davide Piaggio, Damon Hoad, Leandro Pecchia, A personalized toolbox for ophthalmological and neurological research: a pilot study on long covid and neurodegeneration",
      },
      {
        eventName:
          "S6.4. Michela Franzo, Franco Marinozzi, Simona Pascucci, Fabiano Bini, Challenges and outlook to designing cutting-edge mixed reality technologies of human pose estimation",
      },
      {
        eventName:
          "S6.5. Naida Omerović, Anela Hadžifejzović Trnka, Nermina Žiga Smajić, Selma Škrbo, Potentially inappropriate medications in geriatric patients with type 2 diabetes mellitus: practical software solution for healthcare professionals",
      },
      {
        eventName:
          "S6.6. Pedro A. Moreno-Sánchez, Konstantinos Banitsas, Mark van Gils, Maysam Abbod, Addressing Rapidly Aging Society Challenges Through Health 4.0",
      },
      {
        eventName:
          "S6.7. Pio Genovese, Pietro Giambò, Fulvio Abramo, Maura Mancini, Mariana Pastore, Cesare D’Amico, Advancements and Applications in Digital Dentistry: a scoping review",
      },
    ],
  ];

  const currentEventTimeline = eventTimelines[currentDayIndex];

  return (
    <Background>
      <View style={styles.container}>
        <Text style={styles.heading}>
          S6. HEALTH INFORMATICS, E-HEALTH AND TELEMEDICINE (Hall 2)
        </Text>
        <View style={styles.timelineContainer}>
          <ScrollView style={{ flex: 1 }}>
            {currentEventTimeline.map((event, index) => (
              <TouchableOpacity
                key={index}
                style={styles.eventItem}
                onPress={() => {
                  handleEventClick(index);
                }}
              >
                <View style={styles.timelineConnector} />

                <View style={styles.eventContent}>
                  <Text style={styles.eventTime}>{event.time}</Text>
                  <Text
                    style={[
                      styles.eventName,
                      selectedEventIndex === index && styles.selectedEvent,
                    ]}
                  >
                    {event.eventName}
                  </Text>
                  {selectedEventIndex === index && (
                    <Text style={styles.eventDetails}>{event.details}</Text>
                  )}
                </View>
              </TouchableOpacity>
            ))}
          </ScrollView>
        </View>
        <View style={styles.signoutContainer}>
          <TouchableOpacity
            onPress={() => props.navigation.navigate("ConferenceProgram")}
          >
            <Icon
              style={styles.signouticon}
              type={Icons.FontAwesome}
              name="arrow-right"
            />
          </TouchableOpacity>
        </View>
      </View>
    </Background>
  );
};

