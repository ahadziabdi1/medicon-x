import React, { useState } from "react";
import {
  View,
  Text,
  TouchableOpacity,
  StyleSheet,
  Dimensions,
} from "react-native";
import { useNavigation } from "@react-navigation/native";
import Background from "../Background";
import { ScrollView } from "react-native-gesture-handler";
import colors from "../Constants";
import Icon, { Icons } from "../bottomtab/Icon";

const windowWidth = Dimensions.get("window").width;
const windowHeight = Dimensions.get("window").height;

const S24 = (props) => {
  const [currentDayIndex, setCurrentDayIndex] = useState(0);
  const [selectedEventIndex, setSelectedEventIndex] = useState(-1);
  const navigation = useNavigation();

  const handlePreviousDay = () => {
    setCurrentDayIndex((prevIndex) => (prevIndex === 0 ? 3 : prevIndex - 1));
    setSelectedEventIndex(-1);
  };

  const handleNextDay = () => {
    setCurrentDayIndex((prevIndex) => (prevIndex === 3 ? 0 : prevIndex + 1));
    setSelectedEventIndex(-1);
  };

  const handleEventClick = (eventIndex) => {
    setSelectedEventIndex(eventIndex);
  };

  const eventTimelines = [
    [
      {
        eventName:
          "S24.1. Alessio Iannizzotto, Sergio Frumento, Danilo Menicucci, Alejandro Luis Callara, Angelo Gemignani, Enzo Pasquale Scilingo, Alberto Greco, A virtual reality-based setting to investigate how environments and emotionally-laden stimuli interact and compete for accessing consciousness",
      },
      {
        eventName:
          "S24.2. Nicola Carbonaro, Marco Laurino, Alberto Greco, Carlotta Marinai, Francesca Giannetti, Francesca Righetti, Francesco Di Rienzo, Gianluca Rho, Lucia Arcarisi, Michele Zanoletti, Pasquale Bufano, Mario Tesconi, Nicola Sgambelluri, Danilo Menicucci, Carlo Vallati, Alessandro Tognetti, Smart sensors for daily-life data collection toward precision and personalized medicine: the TOLIFE project approach",
      },
      {
        eventName:
          "S24.3. Nerma Džaferović, Adna Softić, Armin Elezović, Faruk Gutic, Haris Vranić, Lejla Gurbeta, Exploring the Progress of Digital Transformation in the Healthcare System of Bosnia and Herzegovina",
      },
      {
        eventName:
          "S24.4. Nikitas Karanikolas, Mirjana Ivanovic, Alda Kika, Ridvan Alimehmeti, Christos Skourlas, Medical Engineering, Health Informatics and Medical Imaging (ME&HI) Profession and Curriculum",
      },
      {
        eventName:
          "S24.5. Hrvoje Belani, Tanja Bedovec, Andreja Matkun, Patients’ Health Information Exchange within the European Union: a Croatian Case Study",
      },
      {
        eventName:
          "S24.6. Sotirios Gyftopoulos, George Drosatos, Leandro Pecchia, Giuseppe Fico, Eleni Kaldoudi, Interaction between pharmaceutical companies and the public during the COVID-19 pandemic – A Twitter analysis",
      },
    ],
  ];

  const currentEventTimeline = eventTimelines[currentDayIndex];

  return (
    <Background>
      <View style={styles.container}>
        <Text style={styles.heading}>
          S24. HEALTH INFORMATICS, E-HEALTH AND TELEMEDICINE (Hall 4)
        </Text>
        <View style={styles.timelineContainer}>
          <ScrollView style={{ flex: 1 }}>
            {currentEventTimeline.map((event, index) => (
              <TouchableOpacity
                key={index}
                style={styles.eventItem}
                onPress={() => {
                  handleEventClick(index);
                }}
              >
                <View style={styles.timelineConnector} />

                <View style={styles.eventContent}>
                  <Text style={styles.eventTime}>{event.time}</Text>
                  <Text
                    style={[
                      styles.eventName,
                      selectedEventIndex === index && styles.selectedEvent,
                    ]}
                  >
                    {event.eventName}
                  </Text>
                  {selectedEventIndex === index && (
                    <Text style={styles.eventDetails}>{event.details}</Text>
                  )}
                </View>
              </TouchableOpacity>
            ))}
          </ScrollView>
        </View>
        <View style={styles.signoutContainer}>
          <TouchableOpacity
            onPress={() => props.navigation.navigate("ConferenceProgram")}
          >
            <Icon
              style={styles.signouticon}
              type={Icons.FontAwesome}
              name="arrow-right"
            />
          </TouchableOpacity>
        </View>
      </View>
    </Background>
  );
};

